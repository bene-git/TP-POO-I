package pessoas;

public class ArrayCarg {
    
    private int carghr;
    private String rgg;
    
    public ArrayCarg(int umaCarg, String umRgg){
        this.carghr = umaCarg;
        this.rgg = umRgg;
    }
    
    public int getCarghr() {
        return carghr;
    }

    public void setCarghr(int carghr) {
        this.carghr = carghr;
    }

    public String getRgg() {
        return rgg;
    }

    public void setRgg(String rgg) {
        this.rgg = rgg;
    }
    
}
